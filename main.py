"""## 셀 7 · Notion API 헬퍼 함수 (수정 불필요)"""

def notion_post(endpoint, payload):
    resp = requests.post(f'{BASE}/{endpoint}', headers=HEADERS, json=payload)
    if resp.status_code not in [200, 201]:
        print(f'❌ API 오류 [{endpoint}]: {resp.status_code} - {resp.text[:300]}')
    return resp.json()

def notion_patch(path, payload):
    resp = requests.patch(f'{BASE}/{path}', headers=HEADERS, json=payload)
    return resp.json()

def notion_get(path, params=None):
    resp = requests.get(f'{BASE}/{path}', headers=HEADERS, params=params)
    return resp.json()

def query_db(db_id, payload=None):
    """DB 전체 조회 (페이지네이션 지원)"""
    results = []
    cursor = None
    while True:
        body = payload.copy() if payload else {}
        if cursor:
            body['start_cursor'] = cursor
        resp = notion_post(f'databases/{db_id}/query', body)
        results.extend(resp.get('results', []))
        if not resp.get('has_more'):
            break
        cursor = resp.get('next_cursor')
    return results

def get_existing_pages(db_id, key_property, key_type='rich_text'):
    """DB에서 key → page_id 매핑 반환"""
    rows = query_db(db_id)
    mapping = {}
    for page in rows:
        props = page['properties']
        try:
            if key_type == 'title':
                key = props[key_property]['title'][0]['text']['content'] if props[key_property]['title'] else ''
            else:
                key = props[key_property]['rich_text'][0]['text']['content'] if props[key_property]['rich_text'] else ''
            if key:
                mapping[key] = page['id']
        except:
            pass
    return mapping

def upsert_page(db_id, existing_map, key, properties):
    """노션 페이지 upsert — 절대 삭제 없이 추가/수정만"""
    if key in existing_map:
        notion_patch(f'pages/{existing_map[key]}', {'properties': properties})
        return 'updated'
    else:
        notion_post('pages', {'parent': {'database_id': db_id}, 'properties': properties})
        return 'inserted'

