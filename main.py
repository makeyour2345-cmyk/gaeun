def calculate_holdings(trades):
    """매매일지 → 보유주식 계산 (평균단가법)"""
    holdings = {}
    for trade in sorted(trades, key=lambda x: x['날짜']):
        ticker = trade['티커']
        qty    = trade['수량']
        price  = trade['단가']
        action = trade['매수매도']
        if ticker not in holdings:
            holdings[ticker] = {'종목이름': trade['종목이름'], '티커': ticker,
                                '분류': trade['분류'], '보유수량': 0, '총매입금액': 0}
        if action == '매수':
            holdings[ticker]['보유수량']   += qty
            holdings[ticker]['총매입금액'] += qty * price
            holdings[ticker]['종목이름']    = trade['종목이름']
            holdings[ticker]['분류']        = trade['분류']
        elif action == '매도' and holdings[ticker]['보유수량'] > 0:
            remain = holdings[ticker]['보유수량']
            avg    = holdings[ticker]['총매입금액'] / remain
            sold   = min(qty, remain)
            holdings[ticker]['보유수량']   -= sold
            holdings[ticker]['총매입금액'] -= sold * avg
    return {k: v for k, v in holdings.items() if v['보유수량'] > 0}

