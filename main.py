import FinanceDataReader as fdr

_price_cache = {}
_usd_krw = None

def get_usd_krw():
    """현재 환율(달러/원) 조회"""
    global _usd_krw
    if _usd_krw: return _usd_krw
    try:
        df = fdr.DataReader('USD/KRW')
        _usd_krw = float(df['Close'].iloc[-1])
        return _usd_krw
    except:
        return 1400.0  # 실패 시 기밴 환율

def get_current_price(ticker, category=''):
    """FinanceDataReader로 현재가 조회 (해외 종목 환율 변환 포함)"""
    if ticker in _price_cache: return _price_cache[ticker]
    if not FDR_AVAILABLE: return None

    try:
        is_us = (category == '해외종목' or ticker.isalpha())
        df = fdr.DataReader(ticker)

        if not df.empty:
            price = float(df['Close'].iloc[-1])
            # 해외 종목(달러 폨기) 경옰 환율 견적 적용
            if is_us:
                rate = get_usd_krw()
                price = price * rate

            _price_cache[ticker] = price
            return price
    except Exception as e:
        print(f'  ⚠️ {ticker} 조회 실패: {e}')
    return None

